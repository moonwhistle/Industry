-- profiles
create table if not exists profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  login_id text unique,
  email text,
  nickname text not null,
  nationality text,
  age int,
  birth_date date,
  nationality_code text,
  nationality_name text,
  user_role text check (user_role in ('근로자', '관리자')) not null,
  industry text check (industry in ('건설업', '제조업', '기타')) not null,
  job_role text,
  manager_type text check (
    manager_type in (
      '안전관리자', '보건관리자', '현장소장',
      '관리감독자', '안전보건총괄책임자', '안전보건관리책임자',
      '기타 관리자'
    )
  ),
  public_id text unique,
  user_code text unique,
  site_role text default 'member'
    check (site_role in ('member', 'staff', 'owner')),
  can_manage_site boolean default false,
  can_grant_admin boolean default false,
  account_status text default 'active'
    check (account_status in ('active', 'suspended', 'banned')),
  report_count int default 0,
  suspended_until timestamptz,
  is_admin boolean default false,
  is_online boolean default false,
  last_seen_at timestamptz,
  last_login_at timestamptz,
  profile_image text,
  created_at timestamptz default now()
);

update profiles
set public_id = coalesce(public_id, 'USER-' || left(id::text, 8)),
    user_code = coalesce(user_code, 'USER-' || left(id::text, 8))
where public_id is null
   or user_code is null;

update profiles
set site_role = 'owner',
    can_manage_site = true,
    can_grant_admin = true,
    is_admin = true
where nickname = '이영군';

