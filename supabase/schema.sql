-- ============================================================

create policy "likes: 전체 읽기 허용"
  on likes for select using (true);

create policy "likes: 본인만 추가"
  on likes for insert with check (auth.uid() = user_id);

create policy "likes: 본인만 삭제"
  on likes for delete using (auth.uid() = user_id);

