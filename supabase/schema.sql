-- ============================================================
-- 댓글 수 자동 갱신
-- ============================================================

create or replace function refresh_post_comment_count()
returns trigger as $$
declare
  affected_post_id bigint;
begin
  if TG_OP = 'DELETE' then
    affected_post_id = old.post_id;
  else
    affected_post_id = new.post_id;
  end if;

  update posts
  set comment_count = (
    select count(*)
    from comments
    where post_id = affected_post_id
      and is_hidden = false
  )
  where id = affected_post_id;

  if TG_OP = 'DELETE' then
    return old;
  end if;

  return new;
end;
$$ language plpgsql security definer;

drop trigger if exists comments_refresh_post_comment_count on comments;

create trigger comments_refresh_post_comment_count
  after insert or update or delete on comments
  for each row execute function refresh_post_comment_count();

-- ============================================================
-- 신고 누적 자동 정지
-- ============================================================

create or replace function update_report_count_and_suspend()
returns trigger as $$
declare
  total_reports int;
begin
  select count(*)
  into total_reports
  from reports
  where reported_user_id = new.reported_user_id
    and status in ('접수', '검토중', '처리완료');

  update profiles
  set report_count = total_reports
  where id = new.reported_user_id;

  if total_reports >= 5 then
    update profiles
    set account_status = 'suspended',
        suspended_until = now() + interval '7 days'
    where id = new.reported_user_id
      and account_status <> 'banned';
  end if;

  return new;
end;
$$ language plpgsql security definer;

drop trigger if exists trigger_update_report_count_and_suspend on reports;

create trigger trigger_update_report_count_and_suspend
  after insert on reports
  for each row execute function update_report_count_and_suspend();

