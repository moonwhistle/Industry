-- ============================================================
-- 좋아요 토글 RPC
-- ============================================================

create or replace function toggle_post_like(post_id_input bigint)
returns int as $$
declare
  existing_like_id bigint;
  new_like_count int;
begin
  if auth.uid() is null then
    raise exception '로그인이 필요합니다.';
  end if;

  select id into existing_like_id
  from likes
  where post_id = post_id_input
    and user_id = auth.uid();

  if existing_like_id is null then
    insert into likes (post_id, user_id)
    values (post_id_input, auth.uid());
  else
    delete from likes
    where id = existing_like_id;
  end if;

  select count(*)
  into new_like_count
  from likes
  where post_id = post_id_input;

  update posts
  set like_count = new_like_count
  where id = post_id_input;

  return new_like_count;
end;
$$ language plpgsql security definer;

