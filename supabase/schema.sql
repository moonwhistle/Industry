create or replace function is_current_user_admin()
returns boolean as $$
  select exists (
    select 1
    from profiles
    where id = auth.uid()
      and (
        site_role in ('owner', 'staff')
        or can_manage_site = true
        or is_admin = true
      )
  );
$$ language sql security definer stable;

