-- ============================================================
-- updated_at 자동 갱신 트리거
-- ============================================================

create or replace function update_updated_at()
returns trigger as $$
begin
  new.updated_at = now();
  return new;
end;
$$ language plpgsql;

create or replace trigger posts_updated_at
  before update on posts
  for each row execute function update_updated_at();

create or replace trigger answers_updated_at
  before update on answers
  for each row execute function update_updated_at();

create or replace trigger answer_opinions_updated_at
  before update on answer_opinions
  for each row execute function update_updated_at();

-- ============================================================
-- 신고 누적 자동 정지
-- ============================================================

create or replace function update_report_count_and_suspend()
returns trigger as $$
declare
  total_reports int;
begin
  select count(*)
  into total_reports
  from reports
  where reported_user_id = new.reported_user_id
    and status in ('접수', '검토중', '처리완료');

  update profiles
  set report_count = total_reports
  where id = new.reported_user_id;

  if total_reports >= 5 then
    update profiles
    set account_status = 'suspended',
        suspended_until = now() + interval '7 days'
    where id = new.reported_user_id
      and account_status <> 'banned';
  end if;

  return new;
end;
$$ language plpgsql security definer;

drop trigger if exists trigger_update_report_count_and_suspend on reports;

create trigger trigger_update_report_count_and_suspend
  after insert on reports
  for each row execute function update_report_count_and_suspend();

