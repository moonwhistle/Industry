-- posts
create table if not exists posts (
  id bigint generated by default as identity primary key,
  title text not null,
  content text not null,
  category_slug text not null references categories(slug),
  author_id uuid references profiles(id) on delete set null,
  image_url text,
  file_url text,
  view_count int default 0,
  like_count int default 0,
  qna_status text default '답변대기' check (qna_status in ('답변대기', '답변완료')),
  accident_type text,
  accident_cause text,
  prevention_plan text,
  is_hidden boolean default false,
  hidden_reason text,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

-- comments
create table if not exists comments (
  id bigint generated by default as identity primary key,
  post_id bigint not null references posts(id) on delete cascade,
  author_id uuid references profiles(id) on delete set null,
  content text not null,
  parent_comment_id bigint references comments(id) on delete cascade,
  is_hidden boolean default false,
  hidden_reason text,
  created_at timestamptz default now()
);

-- likes
create table if not exists likes (
  id bigint generated by default as identity primary key,
  post_id bigint not null references posts(id) on delete cascade,
  user_id uuid not null references profiles(id) on delete cascade,
  created_at timestamptz default now(),
  unique (post_id, user_id)
);

-- answers
create table if not exists answers (
  id bigint generated by default as identity primary key,
  post_id bigint not null references posts(id) on delete cascade,
  author_id uuid references profiles(id) on delete set null,
  content text not null,
  source text,
  is_selected boolean default false,
  is_admin_answer boolean default false,
  is_hidden boolean default false,
  hidden_reason text,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

-- answer opinions
create table if not exists answer_opinions (
  id bigint generated by default as identity primary key,
  answer_id bigint not null references answers(id) on delete cascade,
  author_id uuid references profiles(id) on delete set null,
  opinion_type text check (
    opinion_type in ('이의제기', '추가의견', '근거보완', '오류수정요청')
  ) not null,
  content text not null,
  source text,
  is_hidden boolean default false,
  hidden_reason text,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

-- reports
create table if not exists reports (
  id bigint generated by default as identity primary key,
  target_type text check (
    target_type in ('post', 'comment', 'answer', 'answer_opinion')
  ) not null,
  target_id bigint not null,
  reported_user_id uuid references profiles(id) on delete cascade,
  reporter_id uuid references profiles(id) on delete cascade,
  reason text check (
    reason in (
      '욕설/비방',
      '성희롱',
      '혐오/차별',
      '허위사실',
      '개인정보 노출',
      '회사명/현장명 언급',
      '스팸/광고',
      '기타'
    )
  ) not null,
  detail text,
  status text default '접수'
    check (status in ('접수', '검토중', '처리완료', '반려')),
  created_at timestamptz default now(),
  unique(target_type, target_id, reporter_id)
);

